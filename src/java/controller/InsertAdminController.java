    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("InsertAccount.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        Account a = new Account();
        a.setUsername(request.getParameter("txtAddUser"));
        a.setPassword(request.getParameter("txtAddPass"));
        a.setHost(Boolean.parseBoolean(request.getParameter("host")));
        
        AccountDBContext adb = new AccountDBContext();
        adb.insertAdmin(a);
//        request.getRequestDispatcher("InsertApartment.jsp").forward(request, response);
        response.sendRedirect("manageAdmin");
    }

